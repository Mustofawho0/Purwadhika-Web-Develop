// Primitive Data Type
// -String: Tipe Data yang Dibungkus dengan '' atau "" atau ``
let nama = 'Mustofa'
let umur = '25' // Tetap String
console.log(typeof umur)

let namaDepan = 'Nurul'
let namaBelakang  = 'Mustofa'
console.log('My name is ' + namaDepan + ' ' + namaBelakang + '.')
console.log(`My name is ${namaDepan} ${namaBelakang}.`) // Backtick String Literal (``)

// -Number
let point = 100
let dis = 2.5
console.log(typeof point)
console.log(typeof dis)

// -Boolean (True or False)
let validasi = true
console.log(typeof validasi)

// -Null
let initVal = null

// -Undefined
let initVall = undefined
console.log(typeof initVall)