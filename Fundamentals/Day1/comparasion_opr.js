// Comparasion Operator
// Operator Pembanding
// Resultnya akan Selalu Boolean (True/False)
// > , < , <= , >= , != , !== , == , ===

let num1 = 10
let num2 = 5

console.log(num1 > num2)
console.log(num1 < num2)
console.log(5 <= 5)
console.log(5 >= 5)
console.log('5' != 5)
console.log(5 != 5)
console.log(5 !== '5')

// == : Hanya untuk mengecek Value
console.log(5 == '5')
console.log('abc' == '5')

// === : Mengecek Value dan Tipe Data
console.log(5 === '5')
console.log('abc' === '5')